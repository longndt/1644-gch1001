//1. khai báo biến bằng "var"
var a = 3  //int
var b = 5
var c = a + b
var d = a * b
console.log("a = " + a)
console.log("b = " + b)
console.log("c = a + b = " + c)
console.log("d = a * b = " + d)
var a = "Laptop" //string
console.log (a)
var a = 'o'  //char
console.log (a)
var a = true //boolean
console.log (a)
//2. khai báo biến số (có thể thay đổi được) bằng "let"
let city = "Hanoi"
let country = "Vietnam"
console.log(city + " is the capital of " + country)
//3. khai báo hằng số (giá trị không thay đổi) bằng "const"
const pi = 3.14  //float
console.log("Pi = " + pi)